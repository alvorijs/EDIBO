    {
        public static int fibonace(int n) {
            int a = 0;
            int b = 1;
            //n represent number of fibonace series..
            for (int i = 0; i < n; i++) {

                int temp = a;
                a = b;
                b = temp + b;

            }
         
             return a;


        }

        {
        static void Main(string[] args)
            //call fibonace function in main..

            for (int i = 0; i < 15; i++) {
                 //fibonace series upto 15
